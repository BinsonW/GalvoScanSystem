#define CLK_FREQUENCY_NEW 50000000 //50MHZ
#define CLK_CNT_1MS_NEW 	50000 //50MHZ
#define AUTO_X_PERIOD_N_MAX_NEW 256		//为速度变化的分辨率，即从高速到低速共经历几个等级

//max_frequency 为最高脉冲频率  脉冲/S
//min_frequency 为最低脉冲频率  脉冲/S
//acc_time		为加速时间 ms
//pulse_cnt_sum 为总的脉冲个数

//0x55的定义不变	[0]为X通道开关 [1]为Y通道开关
//					[2]为X通道模式（0连续 1计数）  [3]为Y通道模式（0连续 1计数）
//					[4]为X通道方向（0正向 1反向）	[5]为Y通道方向（0正向 1反向）
//					[11:8]为X通道正向限位IO	[15:12]为X通道负向限位IO	
//					[19:16]为Y通道正向限位IO	[23:20]为Y通道负向限位IO	

void change(UINT  min_frequency,UINT max_frequency, UINT acc_time, UINT pulse_cnt_sum, UINT mode,unsigned int *pBufCmdData,unsigned int *pBufCnt)
{
	UINT acc_sum_pulse_cnt;
	
	UINT period_cnt[256];
	UINT pulse_cnt[256];
	UINT max_sub_min_frequency;
	UINT acc_level_pulse_cnt;
	float delta_frequency;
	float tmp;
	float delta_acc_time;
	UINT current_period,current_frequency,current_pulse;
	UINT i,k=0;
	
	max_sub_min_frequency = max_frequency - min_frequency;
	delta_frequency = max_sub_min_frequency / AUTO_X_PERIOD_N_MAX_NEW;
	
	acc_level_pulse_cnt = (acc_time * CLK_FREQUENCY_NEW) / AUTO_X_PERIOD_N_MAX_NEW;
	
	for(i=0; i<AUTO_X_PERIOD_N_MAX_NEW; i++)
	{
		tmp = i * delta_frequency;
		current_frequency = min_frequency + (UINT)tmp;
		current_period = CLK_FREQUENCY_NEW / current_frequency;
		period_cnt[i] = current_period;
		
		current_pulse = acc_level_pulse_cnt / current_period;
		if(current_pulse == 0)
			pulse_cnt[i] = 1;
		else
			pulse_cnt[i] = current_pulse;
		
		if(mode)		//计数模式
		{
			if(i < AUTO_X_PERIOD_N_MAX_NEW-1)
				acc_sum_pulse_cnt += pulse_cnt[i];		//先不考虑加速过程中脉冲数发送完毕的情况
			else
				pulse_cnt[AUTO_X_PERIOD_N_MAX_NEW-1] = pulse_cnt_sum - acc_sum_pulse_cnt;
		}
	}
	

	for(i=0; i<AUTO_X_PERIOD_N_MAX_NEW; i++)	//将得到的256级速度对应的周期数和脉冲个数写入FPGA内部RAM
	{
		pBufCmdData[k++]=0x56000000 + i;
		pBufCmdData[k++]=0x58000000 + period_cnt[i];
		pBufCmdData[k++]=0x5A000000 + pulse_cnt[i];
		pBufCmdData[k++]=0x53000000 + 1;
	}
	
	(*pBufCnt)=k;
//	return(k-kold);

	//最后启动脉冲发送即可
}

