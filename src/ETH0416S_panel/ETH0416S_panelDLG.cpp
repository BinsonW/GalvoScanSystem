	for(int i=0;i<6;i++)
	{
		if (iLimit6enblestatus[i].x>0 && iLimit6enblestatus[i].y>0) 
		{
			// from left, top, right, and bottom
			//CRect(int l, int t, int r, int b);
			CRect limitPlace(iLimit6enblestatus[i].x, iLimit6enblestatus[i].y, iLimit6enblestatus[i].x+17, iLimit6enblestatus[i].y+17);
			
			if (limitPlace.PtInRect(point)) 
			{
				iLimit6enblestatus[i].onOFF = !(iLimit6enblestatus[i].onOFF);
				// 设置对应使能值
				
			}
		}
	}

	Invalidate(false);
